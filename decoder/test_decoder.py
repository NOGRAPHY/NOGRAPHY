from decoder import decode, decode2


def test_decoding_without_ec():
    encoded = [
        '000', '100', '100', '001', '100', '101', '011', '011',
        '000', '110', '110', '001', '101', '111', '001', '000',
        '000', '101', '011', '101', '101', '111', '011', '100',
        '100', '110', '110', '001', '100', '100']
    expected = 'Hello World'
    assert expected == decode2(encoded)


def test_decoding_with_ec():
    encoded1 = ['000', '000', '000', '010', '010', '000', '001', '010', '001', '011', '000', '000', '000', '011', '011',
                '000', '000', '000', '011', '011', '000', '001', '000', '001', '110', '000', '000', '010', '010', '100',
                '000', '001', '000', '010', '011', '000', '001', '000', '001', '110', '000', '000', '000', '100', '010',
                '000', '000', '000', '011', '011', '000', '000', '001', '000', '010']
    encoded2 = ['100', '000', '000', '010', '010', '000', '001', '010', '001', '011', '000', '000', '000', '011', '011',
                '000', '000', '000', '011', '011', '000', '001', '000', '001', '110', '000', '000', '010', '010', '100',
                '000', '001', '000', '010', '011', '000', '001', '000', '001', '110', '000', '000', '000', '100', '010',
                '000', '000', '000', '011', '011', '000', '000', '001', '000', '010']
    expected = 'Hello World'
    assert expected == decode(encoded1)
    assert expected == decode(encoded2)


if __name__ == '__main__':
    test_decoding_with_ec()
